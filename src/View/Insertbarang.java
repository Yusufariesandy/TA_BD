    private void btnInsertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInsertActionPerformed
        Barang_07031 barang = new Barang_07031();
        
        try {
            barang.setId_Barang(Integer.parseInt(this.tfId.getText()));
            barang.setNama_Barang(this.tfNama.getText());
            barang.setJenis_Barang(this.tfJenis.getText());
            barang.setHarga(Integer.parseInt(this.tfHarga.getText()));
            barang.setStock(Integer.parseInt (this.tfStock.getText()));
            
            
            this.barang.getDataBarang_07031();
            this.barang.insertBarang_07031(barang);
            this.ShowTableBarang_07031();
        } catch (SQLException  err) {
            System.out.println(err);
        }
        
    }//GEN-LAST:event_btnInsertActionPerformed

