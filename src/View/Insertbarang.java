    private void ShowTableBarang_07031() throws SQLException {
        DefaultTableModel dtmBarang = new DefaultTableModel(new String[]
        {"Id Barang","Nama Barang","Jenis Barang","Harga","Stock",},0);
        dtmBarang.setRowCount(0); 
        
        for(Barang_07031 dp : this.barang.getDataBarang_07031()){
            dtmBarang.addRow(new String[]{dp.getId_Barang().toString(), dp.getNama_Barang().toString(),dp.getJenis_Barang().toString(), 
                dp.getHarga().toString(),dp.getStock().toString()});
        }
        this.tblBarang.setModel(dtmBarang);
    }
